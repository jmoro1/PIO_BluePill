void DHT_Init(DHT *dht, uint8_t count, GPIO_TypeDef *GPIOx, uint16_t GPIO_Pin){

    dht->pin = GPIO_Pin;
    dht->gpiox = GPIOx;
    dht->count = count;
    dht->lastReadTime = 0;
    dht->maxCycles = microsecondsToClockCycles(1000); //1ms timeout for reading pulses from the sensor
    dht->lastResult = FALSE;
    dht->pullTime = 55; //us (microseconds) before the sensor reading starts

    //Setting GPIO pin as input
    GPIO_CustomHandler(dht->gpiox, dht->pin, INPUT);

}

static uint32_t DHT_ExpectPulse(DHT *dht, bool level){
	uint32_t count=0;
	uint32_t maxCycles = dht->maxCycles;
  bool PulseRead = HAL_GPIO_ReadPin(dht->gpiox, dht->pin);
	while(PulseRead == level){
		if(count++ >= maxCycles){
			return 0; // TIMEOUT
		}
	}
	return count;
}

bool DHT_Read(DHT *dht, bool force){
  uint32_t currenttime = HAL_GetTick();
  if(!force && ((currenttime - dht->lastReadTime) < 2000)){ // if force is false and at least 2 seg passed from the previous read
    return dht->lastResult;
  }
  
  dht->lastReadTime = currenttime;

  /*Start the signal*/
  GPIO_CustomHandler(dht->gpiox, dht->pin, OUTPUT);

}

